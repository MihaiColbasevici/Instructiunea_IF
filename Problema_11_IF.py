a, b, c = input("Dati 3 numere: ").split()
a = int(a)
b = int(b)
c = int(c)
if (a % 2 == 0):
    print(a, "par.")
elif (a % 2 != 0):
    print(a, "impar.")
if (b % 2 == 0):
    print(b, "par.")
elif (b % 2 != 0):
    print(b, "impar.")
if (c % 2 == 0):
    print(c, "par.")
elif (c % 2 != 0):
    print(c, "impar.")
