a = int(input("Cate petale are margareta? \t"))
if (a % 10 == 1) or (a % 10 == 6):
    print("Ma iubeste un pic.")
elif (a % 10 == 2) or (a % 10 == 7):
    print("Ma iubeste mult.")
elif (a % 10 == 3) or (a % 10 == 8):
    print("Ma iubeste cu pasiune.")
elif (a % 10 == 4) or (a % 10 == 9):
    print("Ma iubeste la nebunie.")
elif (a % 10 == 5) or (a % 10 == 0):
    print("Nu ma iubeste deloc.")